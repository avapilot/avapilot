def _describe_func(func: dict) -> str:
    """Generate a short description of a function from its ABI."""
    inputs = func.get("inputs", [])
    outputs = func.get("outputs", [])
    
    in_str = ", ".join(f"{i.get('name', '?')}: {i['type']}" for i in inputs)
    out_str = ", ".join(o["type"] for o in outputs)
    
    mutability = func.get("stateMutability", "")
    payable = " 💰 payable" if mutability == "payable" else ""
    
    return f"({in_str}) → {out_str}{payable}"
